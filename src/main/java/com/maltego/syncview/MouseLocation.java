package com.maltego.syncview;

/**
 *
 * @author gertgeringer
 */
public class MouseLocation {

    private String user;
    private float x;
    private float y;

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

}
